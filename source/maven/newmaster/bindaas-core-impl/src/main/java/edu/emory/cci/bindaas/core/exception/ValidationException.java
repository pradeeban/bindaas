package edu.emory.cci.bindaas.core.exception;

public class ValidationException extends Exception {

	public ValidationException() {
		super();
		
	}

	public ValidationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public ValidationException(String arg0) {
		super(arg0);
		
	}

	public ValidationException(Throwable arg0) {
		super(arg0);
		
	}

	
	private static final long serialVersionUID = 4111016529349824409L;

}
