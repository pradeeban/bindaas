package edu.emory.cci.bindaas.core.exception;

public class ExecutionTaskException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -4077135102331095701L;

	public ExecutionTaskException() {
		super();
		
	}

	public ExecutionTaskException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public ExecutionTaskException(String message) {
		super(message);
		
	}

	public ExecutionTaskException(Throwable cause) {
		super(cause);
		
	}

}
